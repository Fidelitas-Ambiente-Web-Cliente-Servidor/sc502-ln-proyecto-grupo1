<section class="faq-container">

    <h2 class="titulo-faq">Dudas más comunes</h2>

    <div class="faq-item">
        <button class="faq-question">¿Quiénes pueden adoptar?</button>
        <div class="faq-answer">
            En Costa Rica, pueden adoptar personas mayores de edad que cumplan 
            con las condiciones establecidas en el Código de Familia y bajo la 
            supervisión del Patronato Nacional de la Infancia. 
            Esto incluye tanto personas solteras como parejas casadas o en unión de hecho.
            Según la normativa vigente, los solicitantes deben demostrar estabilidad emocional, 
            económica y social, así como la capacidad de brindar un entorno seguro 
            y adecuado para el desarrollo integral del menor. Además, se evalúa que exista una 
            diferencia de edad razonable entre el adoptante y el adoptado, conforme a criterios legales.
        </div>
    </div>

    <div class="faq-item">
        <button class="faq-question">¿Cuáles son los requisitos?</button>
        <div class="faq-answer">
         Los requisitos para adoptar en Costa Rica están regulados principalmente por el Código de 
         Familia y las disposiciones del Patronato Nacional de la Infancia. 
         Entre los más importantes se encuentran:   
            <ul>
                <li>Ser mayor de edad</li>
                <li>No tener antecedentes penales</li>
                <li>Estabilidad económica</li>
                <li>Evaluación psicológica aprobada</li>
                <li>Documentación legal completa</li>
            </ul>
        Estos requisitos buscan garantizar el interés superior del niño,
         principio fundamental en la legislación costarricense.
        </div>
    </div>

    <div class="faq-item">
        <button class="faq-question">¿Cómo es el proceso de adopción?</button>
        <div class="faq-answer">
            El proceso de adopción en Costa Rica es un procedimiento formal regulado 
            por el Código de Familia y ejecutado por el Patronato Nacional de la Infancia.
            Este proceso incluye varias etapas: inscripción de la solicitud, entrega 
            y verificación de documentos, evaluaciones psicológicas y sociales, determinación 
            de idoneidad, asignación del menor y seguimiento posterior.
            Durante todo el proceso, se prioriza el principio legal del interés superior del menor, 
            garantizando que la adopción se realice en condiciones que favorezcan su bienestar 
            físico, emocional y social.
    </div>

    <div class="faq-item">
        <button class="faq-question">¿Cuánto dura el proceso?</button>
        <div class="faq-answer">
            El tiempo del proceso de adopción en Costa Rica puede variar dependiendo 
            de cada caso y de los procedimientos establecidos por el Patronato 
            Nacional de la Infancia.
            Generalmente, puede tardar entre seis meses y más de un año, ya que 
            incluye múltiples evaluaciones, validaciones legales y disponibilidad 
            de menores en condición de adoptabilidad. Este tiempo es necesario 
            para asegurar que se cumplan todos los requisitos legales y que la 
            decisión sea la más adecuada para el menor.
        </div>
    </div>

    <div class="faq-item">
        <button class="faq-question">¿La adopción tiene costo?</button>
        <div class="faq-answer">
            En Costa Rica, la adopción es un proceso gratuito, ya que está 
            regulado por el Estado a través del Patronato Nacional de la Infancia.
            Sin embargo, pueden existir gastos indirectos relacionados con
            la obtención de documentos legales, certificaciones, evaluaciones 
            médicas o trámites administrativos. Es importante destacar que no 
            se paga por adoptar, ya que esto está prohibido por la 
            legislación costarricense.
        </div>
    </div>

    <div class="faq-item">
        <button class="faq-question">¿Qué pasa después de adoptar?</button>
        <div class="faq-answer">
            Después de concretarse la adopción, se realiza un proceso de 
            seguimiento por parte del Patronato Nacional de la Infancia.
            Este seguimiento tiene como objetivo verificar la 
            correcta adaptación del menor en su nuevo entorno familiar, 
            asegurando su bienestar integral. Además, se brinda 
            acompañamiento y orientación a la familia adoptiva 
            para fortalecer el vínculo y garantizar el cumplimiento 
            de los derechos del niño establecidos en la legislación nacional.
        </div>
    </div>

    
