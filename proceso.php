                    información de contacto y motivo de adopción.
                </p>
            </div>
        </li>
        <li>
            <span class="paso-numero">2</span>
            <div class="paso-contenido">
                <h4>Revisión administrativa</h4>
                <p>
                    Nuestro equipo verifica que la información sea clara, completa y
                    cumpla con los requisitos mínimos, como la edad y los datos de identificación.
                </p>
            </div>
        </li>
        <li>
            <span class="paso-numero">3</span>
            <div class="paso-contenido">
